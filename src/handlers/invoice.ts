/** Build inline keyboard for party selection (max 5 per page). */
function partySelectionKeyboard(
  parties: Array<{ item: { id: number | string; party_name: string }; score: number }>,
): ReturnType<typeof Markup.inlineKeyboard> {
  const rows = parties.slice(0, 5).map((match) => {
    const partyName = match.item.party_name;
    return [Markup.button.callback(partyName, `inv_party:${partyName}`)];
  });

  // Show "no results" row if empty
  if (rows.length === 0) {
    rows.push([Markup.button.callback('❌ No parties found', 'noop')]);
  }

  rows.push([Markup.button.callback('🏠 Main Menu', 'start')]);
  return Markup.inlineKeyboard(rows);
}

/** Build voucher pagination keyboard. */
function voucherPaginationKeyboard(
  chatId: number,
  partyName: string,
  page: number,
  totalPages: number,
): ReturnType<typeof Markup.inlineKeyboard> {
  const rows: ReturnType<typeof Markup.button.callback>[][] = [];

  // Older / Newer buttons
  const navButtons: ReturnType<typeof Markup.button.callback>[] = [];
  if (page > 0) {
    navButtons.push(Markup.button.callback('⬅️ Older 5', `inv_page:${partyName}:${page - 1}`));
  }
  if (page < totalPages - 1) {
    navButtons.push(Markup.button.callback('➡️ Newer 5', `inv_page:${partyName}:${page + 1}`));
  }
  if (navButtons.length > 0) {
    rows.push(navButtons);
  }

  rows.push([Markup.button.callback('🔍 Search Other Party', 'invoice')]);
  rows.push([Markup.button.callback('🏠 Main Menu', 'start')]);
  return Markup.inlineKeyboard(rows);
}

/** Build format selection keyboard for a specific voucher. */
function formatSelectionKeyboard(voucherId: number | string): ReturnType<typeof Markup.inlineKeyboard> {
  return Markup.inlineKeyboard([
    [
      Markup.button.callback('📄 PDF', `inv_fmt_pdf:${voucherId}`),
      Markup.button.callback('📊 Excel', `inv_fmt_excel:${voucherId}`),
    ],
    [Markup.button.callback('🔙 Back to Vouchers', `inv_back:${voucherId}`)],
    [Markup.button.callback('🏠 Main Menu', 'start')],
  ]);
}

/** Generate a basic PDF invoice using pdfkit. */
async function generateInvoicePdf(
  voucher: any,
  outputPath: string,
): Promise<void> {
  return new Promise((resolve, reject) => {
    try {
      const doc = new PDFDocument({ size: 'A4', margin: 50 });
      const stream = fs.createWriteStream(outputPath);

      doc.pipe(stream);

      // Header
      doc.fontSize(20).font('Helvetica-Bold').text('TallyOnMobile', { align: 'center' });
      doc.fontSize(12).font('Helvetica').text('Invoice / Voucher', { align: 'center' });
      doc.moveDown();

      // Horizontal rule
      doc.moveTo(50, doc.y).lineTo(545, doc.y).stroke();
      doc.moveDown();

      // Voucher details
      doc.fontSize(11).font('Helvetica-Bold');
      doc.text(`Voucher #: `, { continued: true }).font('Helvetica').text(`${voucher.voucher_number || '—'}`);
      doc.font('Helvetica-Bold').text(`Date: `, { continued: true }).font('Helvetica').text(`${formatDate(voucher.voucher_date || voucher.date)}`);
      doc.font('Helvetica-Bold').text(`Type: `, { continued: true }).font('Helvetica').text(`${formatVoucherType(voucher.voucher_type)}`);
      doc.moveDown();

      // Party
      doc.font('Helvetica-Bold').text(`Party: `, { continued: true }).font('Helvetica').text(`${voucher.party_name || '—'}`);
      doc.moveDown();

      // Horizontal rule
      doc.moveTo(50, doc.y).lineTo(545, doc.y).stroke();
      doc.moveDown();

      // Amount
      doc.fontSize(14).font('Helvetica-Bold');
      doc.text(`Amount: ${formatIndian(Number(voucher.amount) || 0)}`, { align: 'right' });
      doc.moveDown();

      // Footer
      doc.moveTo(50, doc.y).lineTo(545, doc.y).stroke();
      doc.moveDown();
      doc.fontSize(9).font('Helvetica').text(
        'Generated by TallyOnMobile AI Bot • Powered by Nous Research',
        { align: 'center' },
      );

      doc.end();

      stream.on('finish', () => resolve());
      stream.on('error', reject);
    } catch (err) {
      reject(err);
    }
  });
}

