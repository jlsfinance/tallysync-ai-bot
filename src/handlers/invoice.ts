/**
 * /invoice command handler.
 * If a party name is provided inline, search and show matching parties.
 * Otherwise, ask for a party name.
 */
export async function invoiceCommand(ctx: Context): Promise<void> {
  const chatId = ctx.chat!.id;
  const text = ctx.message && 'text' in ctx.message ? ctx.message.text : '';
  const args = text.replace(/^\/invoice\s*/i, '').trim();

  if (args) {
    // Direct party query: /invoice <party>
    await searchAndShowParties(ctx, args);
  } else {
    // Ask for party name and set state
    setState(chatId, ConversationState.AWAITING_PARTY, { reportType: 'invoice' });
    await ctx.replyWithMarkdown(
      '🧾 *Invoice / Voucher Search*\n\nPlease enter a *party name* to search for invoices:',
      {
        reply_markup: {
          inline_keyboard: [
            [{ text: '🏠 Main Menu', callback_data: 'start' }],
          ],
        },
      },
    );
  }
}

/**
 * Search parties by name and show matching results as inline keyboard.
 */
export async function searchAndShowParties(
  ctx: Context,
  query: string,
): Promise<void> {
  const chatId = ctx.chat!.id;
  logger.info('Invoice: searching parties', { chatId, query });

  await ctx.replyWithMarkdown(`🔍 *Searching parties for:* \`${escapeMd(query)}\`…`);

  try {
    const matches = await searchParties(query, { maxResults: 10 });
    const parties = matches.slice(0, 5);

    // Store in session for later reference
    storeSession(chatId, {
      lastSearchedParty: query,
      lastResults: matches.map((m) => m.item),
      currentPage: 0,
      pageSize: VOUCHERS_PER_PAGE,
    });

    if (parties.length === 0) {
      await ctx.replyWithMarkdown(
        `❌ No parties found matching \`${query}\`.\n\nPlease try a different name.`,
        {
          reply_markup: {
            inline_keyboard: [
              [{ text: '🔍 Try Again', callback_data: 'invoice' }],
              [{ text: '🏠 Main Menu', callback_data: 'start' }],
            ],
          },
        },
      );
      return;
    }

    const partyLines = parties.map(
      (p, i) => `${i + 1}. ${escapeMd(p.item.party_name)} (${p.method}, score: ${p.score})`,
    );

    const msg = [
      `🧾 *Invoice Search*`,
      '',
      `Found *${matches.length}* match(es) for \`${query}\`:`,
      '',
      ...partyLines,
      '',
      'Select a party to view their recent vouchers 👇',
    ].join('\n');

    await ctx.replyWithMarkdown(msg, partySelectionKeyboard(parties));
  } catch (err: any) {
    logger.error('Invoice: party search error', { chatId, error: err?.message });
    await ctx.replyWithMarkdown(
      '⚠️ *Error searching parties.* Please try again later.',
    );
  }
}

/**
 * Handle party selection from inline keyboard.
 * Fetches last 5 vouchers for the selected party.
 */
export async function onPartySelected(ctx: Context, partyName: string): Promise<void> {
  const chatId = ctx.chat!.id;
  logger.info('Invoice: party selected', { chatId, partyName });

  storeSession(chatId, { lastSearchedParty: partyName });

  await ctx.replyWithMarkdown(`📄 *Fetching vouchers for* \`${partyName}\`…`);

  try {
    const vouchers = await fuzzyGetVouchers(partyName, { limit: 20 });
    storeResults(chatId, vouchers, { pageSize: VOUCHERS_PER_PAGE });

    if (vouchers.length === 0) {
      await ctx.replyWithMarkdown(
        `📭 No vouchers found for \`${partyName}\`.`,
        {
          reply_markup: {
            inline_keyboard: [
              [{ text: '🔍 Search Other Party', callback_data: 'invoice' }],
              [{ text: '🏠 Main Menu', callback_data: 'start' }],
            ],
          },
        },
      );
      return;
    }

    await showVoucherPage(ctx, chatId, 0);
  } catch (err: any) {
    logger.error('Invoice: fetch vouchers error', { chatId, error: err?.message });
    await ctx.replyWithMarkdown('⚠️ *Error fetching vouchers.* Please try again.');
  }
}

/**
 * Show a page of vouchers.
 */
async function showVoucherPage(ctx: Context, chatId: number, page: number): Promise<void> {
  const { items, page: currentPage, totalPages, totalResults } = getResultsPage(chatId, page);

  if (items.length === 0) {
    await ctx.replyWithMarkdown('📭 No vouchers to display.');
    return;
  }

  const session = getSession(chatId);
  const partyName = session.lastSearchedParty ?? 'Selected Party';

  const voucherLines = items.map((v: any, i: number) => {
    const idx = currentPage * VOUCHERS_PER_PAGE + i + 1;
    return [
      `${idx}. ${formatVoucherType(v.voucher_type)}`,
      `   📅 ${formatDate(v.voucher_date || v.date)}`,
      `   💰 ${formatIndian(Number(v.amount) || 0)}`,
      `   🆔 \`${v.voucher_number || '—'}\``,
    ].join('\n');
  });

  const msg = [
    `🧾 *Vouchers — ${partyName}*`,
    `📄 Page ${currentPage + 1} of ${totalPages} (${totalResults} total)`,
    '',
    '━━━━━━━━━━━━━━━━━━',
    ...voucherLines,
    '',
    'Select a voucher to download 👇',
  ].join('\n');

  // Build voucher selection keyboard
  const rows: ReturnType<typeof Markup.button.callback>[][] = items.map((v: any) => {
    const label = `${v.voucher_number || '#' + v.id} • ${formatIndian(Number(v.amount) || 0)}`;
    return [Markup.button.callback(label, `inv_vouch:${v.id}`)];
  });

  // Navigation
  const navButtons: ReturnType<typeof Markup.button.callback>[] = [];
  if (currentPage > 0) {
    navButtons.push(Markup.button.callback('⬅️ Older 5', `inv_page:${partyName}:${currentPage - 1}`));
  }
  if (currentPage < totalPages - 1) {
    navButtons.push(Markup.button.callback('➡️ Newer 5', `inv_page:${partyName}:${currentPage + 1}`));
  }
  if (navButtons.length > 0) {
    rows.push(navButtons);
  }

  rows.push([Markup.button.callback('🔍 Search Other Party', 'invoice')]);
  rows.push([Markup.button.callback('🏠 Main Menu', 'start')]);

  await ctx.replyWithMarkdown(msg, Markup.inlineKeyboard(rows));
}

/**
 * Handle voucher pagination.
 */
export async function onVoucherPage(
  ctx: Context,
  partyName: string,
  page: number,
): Promise<void> {
  const chatId = ctx.chat!.id;
  storeSession(chatId, { lastSearchedParty: partyName });
  await showVoucherPage(ctx, chatId, page);
}

/**
 * Handle voucher selection – ask format (PDF or Excel).
 */
export async function onVoucherSelected(ctx: Context, voucherId: string): Promise<void> {
  const chatId = ctx.chat!.id;

  // Store selected voucher ID in session
  setState(chatId, ConversationState.AWAITING_FORMAT, {
    lastSearchedVoucher: voucherId,
  });

  // Try to fetch the voucher from stored results
  const session = getSession(chatId);
  const results = session.lastResults ?? [];
  const voucher = results.find((v: any) => String(v.id) === String(voucherId));

  if (!voucher) {
    await ctx.replyWithMarkdown('⚠️ *Voucher data not found.* Please search again.');
    return;
  }

  const msg = [
    `📄 *Voucher Selected*`,
    '',
    `🧾 Voucher #: \`${voucher.voucher_number || '—'}\``,
    `📅 Date: ${formatDate(voucher.voucher_date || voucher.date)}`,
    `💳 Type: ${formatVoucherType(voucher.voucher_type)}`,
    `💰 Amount: ${formatIndian(Number(voucher.amount) || 0)}`,
    '',
    'Choose a format to download 👇',
  ].join('\n');

  await ctx.replyWithMarkdown(msg, formatSelectionKeyboard(voucherId));
}

/**
 * Generate and send PDF for a selected voucher.
 */
export async function onGeneratePdf(ctx: Context, voucherId: string): Promise<void> {
  const chatId = ctx.chat!.id;
  const session = getSession(chatId);
  const results = session.lastResults ?? [];
  const voucher = results.find((v: any) => String(v.id) === String(voucherId));

  if (!voucher) {
    await ctx.replyWithMarkdown('⚠️ *Voucher not found.* Please search again.');
    return;
  }

  await ctx.replyWithMarkdown('📄 *Generating PDF…* Please wait ⏳');

  try {
    const fileName = `invoice_${voucher.voucher_number || voucher.id}_${Date.now()}.pdf`;
    const outputDir = path.resolve(__dirname, '..', '..', 'temp');
    if (!fs.existsSync(outputDir)) {
      fs.mkdirSync(outputDir, { recursive: true });
    }
    const outputPath = path.join(outputDir, fileName);

    await generateInvoicePdf(voucher, outputPath);

    // Send the PDF
    await ctx.replyWithDocument(
      { source: outputPath, filename: fileName },
      {
        caption: [
          `🧾 *Invoice: ${voucher.voucher_number || '—'}*`,
          `📅 ${formatDate(voucher.voucher_date || voucher.date)}`,
          `💰 ${formatIndian(Number(voucher.amount) || 0)}`,
        ].join('\n'),
        parse_mode: 'Markdown',
      },
    );

    // Cleanup temp file
    fs.unlink(outputPath, (err) => {
      if (err) logger.warn('Failed to delete temp PDF', { path: outputPath, error: err.message });
    });

    // Return to voucher list
    await showVoucherPage(ctx, chatId, session.currentPage ?? 0);
  } catch (err: any) {
    logger.error('Invoice: PDF generation error', { chatId, error: err?.message });
    await ctx.replyWithMarkdown('⚠️ *Error generating PDF.* Please try again.');
  }
}

